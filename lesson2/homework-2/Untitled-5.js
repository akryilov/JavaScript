// 5. Реализовать основные 4 арифметические операции в виде функций с двумя параметрами. Обязательно использовать оператор return.


function sum(a, b) {
    return a + b;
}

function sub(a, b) {
    return a - b;
}

function div(a, b) {
    return a / b;
}

function mult(a, b) {
    return a * b;
}


var a = 1;
var b = 2;

alert(sum(a, b));
alert(sub(a, b));
alert(div(a, b));
alert(mult(a, b));